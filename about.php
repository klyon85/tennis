 	<h1>About Us</h1>
	<p>The Indoor Tennis Center at the University of Mary Washington is a tremendous facility that benefits the entire university and Fredericksburg communities.  The tennis center as a whole is free to faculty, staff, and students.  The men’s and women’s tennis teams practice and compete throughout the year at this outstanding complex of 18 courts.  The UTC also allows UMW to host numerous collegiate events, providing essential indoor backup to the 12 outdoor courts.   Students benefit in numerous ways, including PE classes, club tennis practice and competition, and open play.  In addition, the tennis courts are open to the Fredericksburg community at very reasonable rates.  Finaly, along with general court time, our staff provides  tennis lessons, clinics, and summer camps, taught by professionals, to adults and juniors throughout the year.</p>

